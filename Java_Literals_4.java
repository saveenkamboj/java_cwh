package com.company;

public class Java_Literals_4 {
    public static void main(String[] args) {
        System.out.println("Java Literals");

//        Integral
        byte a = 4;
        int b = 405;
        short age = 95;
        long longNumber = 44599939499L; //Add L at last

//        Character
        char c = 'A';

//        Float(Decimals
        float f1 = 34.4f;  //f
        double f2 = 49.45333d; //d

//        Boolean
        boolean b1 = true;

//        String (Special Support)
        String name = "Saveen Kamboj";

        System.out.println(name);
    }


}
